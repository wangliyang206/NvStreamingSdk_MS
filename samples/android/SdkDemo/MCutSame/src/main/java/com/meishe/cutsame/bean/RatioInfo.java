public class RatioInfo {
    /**
     * The corresponding RATIO
     * 对应的RATIO值
     */
    private int tag;
    /**
     * Name of Ratio
     * 比例名称
     */
    private String name;

    public int getTag() {
        return tag;
    }

    public void setTag(int tag) {
        this.tag = tag;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
